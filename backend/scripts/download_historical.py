"""
Download Historical — OHLCV + Macro TimescaleDB Loader
========================================================
CLI script to populate TimescaleDB with historical OHLCV and macro data.

Usage:
    python -m backend.scripts.download_historical \\
        --tickers SPY,QQQ,AAPL,NVDA \\
        --tf 1d,1h \\
        --years 5 \\
        --include-macro

Harmonization Rules Applied:
- R1: All timestamps converted to UTC
- R2: Columns normalized to lowercase (open, high, low, close, volume)
- R7: Append-only with deduplication (ON CONFLICT DO NOTHING)

Note: Weekly candles are auto-generated by TimescaleDB continuous aggregates.
"""
