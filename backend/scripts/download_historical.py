logger = logging.getLogger(__name__)
logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(name)s] %(message)s")


def harmonize_yfinance(df: pd.DataFrame, ticker: str) -> pd.DataFrame:
    """Apply harmonization rules R1 + R2 to yfinance DataFrame."""
    if df.empty:
        return df

    # Flatten MultiIndex if present (yfinance multi-ticker download)
    if isinstance(df.columns, pd.MultiIndex):
        df = df.xs(ticker, level=1, axis=1)

    # R2: lowercase columns
    df.columns = [c.lower() for c in df.columns]

    # Keep only OHLCV
    required = ["open", "high", "low", "close", "volume"]
    available = [c for c in required if c in df.columns]
    df = df[available].copy()

    # R1: convert to UTC
    if df.index.tz is not None:
        df.index = df.index.tz_convert("UTC")
    else:
        df.index = df.index.tz_localize("UTC")

    df.index.name = "timestamp"

    # Dtypes
    for col in ["open", "high", "low", "close"]:
        if col in df.columns:
            df[col] = df[col].astype("float64")
    if "volume" in df.columns:
        df["volume"] = df["volume"].fillna(0).astype("int64")

    # Drop rows with NaN OHLC
    df.dropna(subset=["open", "high", "low", "close"], inplace=True)

    return df


def enrich_with_alpaca(ticker: str, start, end, store: TimescaleDataStore) -> int:
    """Enrich existing bars with trade_count + vwap from Alpaca SDK."""
    import os
    from datetime import datetime, UTC

    api_key = os.environ.get("ALPACA_API_KEY", "")
    if not api_key:
        logger.debug("ALPACA_API_KEY not set — skipping enrichment")
        return 0

    try:
        from alpaca.data.historical import StockHistoricalDataClient
        from alpaca.data.requests import StockBarsRequest
        from alpaca.data.timeframe import TimeFrame

        client = StockHistoricalDataClient(
            api_key, os.environ.get("ALPACA_SECRET_KEY", "")
        )
        request = StockBarsRequest(
            symbol_or_symbols=ticker,
            timeframe=TimeFrame.Day,
            start=datetime.combine(start, datetime.min.time()).replace(tzinfo=UTC),
            end=datetime.combine(end, datetime.max.time()).replace(tzinfo=UTC),
            limit=10000,
        )
        bars = client.get_stock_bars(request)
        if not bars or ticker not in bars.data:
            return 0

        conn = store._conn()
        updated = 0
        try:
            with conn.cursor() as cur:
                for bar in bars.data[ticker]:
                    vwap = float(bar.vwap) if hasattr(bar, 'vwap') and bar.vwap else None
                    tc = int(bar.trade_count) if hasattr(bar, 'trade_count') and bar.trade_count else None
                    if vwap is None and tc is None:
                        continue
                    cur.execute(
                        """UPDATE market.ohlcv_bars
                           SET vwap = %s, trade_count = %s
                           WHERE ticker = %s AND timeframe = '1d'
                           AND time::date = %s""",
                        (vwap, tc, ticker, bar.timestamp.date()),
                    )
                    updated += cur.rowcount
            conn.commit()
        except Exception:
            conn.rollback()
            raise
        finally:
            store._put(conn)

        if updated > 0:
            logger.info(f"  ↳ Alpaca enrichment: {updated} bars got trade_count + vwap")
        return updated

    except ImportError:
        logger.debug("alpaca-py not installed — skipping enrichment")
        return 0
    except Exception as e:
        logger.warning(f"  ↳ Alpaca enrichment failed for {ticker}: {e}")
        return 0


def download_ticker(ticker: str, tf: str, years: int, store: TimescaleDataStore) -> int:
    """Download and vault a single ticker/timeframe."""
    try:
        import yfinance as yf
    except ImportError:
        logger.error("yfinance not installed: pip install yfinance")
        return 0

    # Check last date for incremental download
    last_date = store.bars_last_date(ticker, tf)

    if last_date:
        start_dt = last_date + timedelta(days=1)
        period = None
        start_str = start_dt.strftime("%Y-%m-%d")
        logger.info(f"Incremental: {ticker}/{tf} from {start_str}")
        df = yf.download(
            ticker, start=start_str,
            interval=tf, progress=False, auto_adjust=True,
        )
    else:
        if tf == "1h":
            # Yahoo limits 1h to ~730 days
            period = "730d"
        else:
            period = f"{years}y"
        logger.info(f"Full download: {ticker}/{tf} period={period}")
        df = yf.download(
            ticker, period=period,
            interval=tf, progress=False, auto_adjust=True,
        )

    if df.empty:
        logger.warning(f"No data returned for {ticker}/{tf}")
        return 0

    df = harmonize_yfinance(df, ticker)
    store.save_bars(ticker, tf, df)

    # Enrich with Alpaca trade_count + vwap (yfinance doesn't provide these)
    if tf == "1d":
        first_date = df.index.min().date() if hasattr(df.index.min(), 'date') else df.index.min()
        last_date = df.index.max().date() if hasattr(df.index.max(), 'date') else df.index.max()
        enrich_with_alpaca(ticker, first_date, last_date, store)

    return len(df)


def download_macro(store: TimescaleDataStore) -> None:
    """Download VIX and yield curve data."""
    try:
        import yfinance as yf
    except ImportError:
        logger.error("yfinance not installed")
        return

    # VIX
    logger.info("Downloading VIX...")
    vix = yf.download("^VIX", period="5y", interval="1d", progress=False)
    if not vix.empty:
        vix = harmonize_yfinance(vix, "^VIX")
        store.save_macro("vix", vix)
        logger.info(f"VIX: {len(vix)} bars saved")

    # Yields
    logger.info("Downloading yields (TNX, IRX)...")
    tnx = yf.download("^TNX", period="5y", interval="1d", progress=False)
    irx = yf.download("^IRX", period="5y", interval="1d", progress=False)

    if not tnx.empty and not irx.empty:
        tnx = harmonize_yfinance(tnx, "^TNX")
        irx = harmonize_yfinance(irx, "^IRX")
        yields = pd.DataFrame({
            "yield_10y": tnx["close"],
            "yield_3m": irx["close"],
        }).dropna()
        store.save_macro("yields", yields)
        logger.info(f"Yields: {len(yields)} bars saved")


